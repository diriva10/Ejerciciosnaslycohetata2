nota=float(input("ingrese la nota de su respectiva materia:"))

if nota>=4.6 and nota<=5.0:
    print("Excelente")
elif nota>=3.6 and nota<=4.6:
    print("Buena")
elif nota>=3.0 and nota<=3.6:
    print("Aceptable")
elif nota>=2.0 and nota<=3.0:
    print("Insuficiente")
elif nota<=2.0 and nota>=0.0:
    print("Deficiente")

       

